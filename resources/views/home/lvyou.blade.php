                        <p>济宁小北湖公园（太白湖旅游度假区）位于中国济宁城区南部，地处苏、鲁、豫、皖四省交界处，南临烟波浩淼的微山湖，北依五岳之尊的泰山，东连孔子故里曲阜，西临水泊梁山。是国家AAAA级旅游区，山东省级旅游度假区。</p>
                        <p>北湖东依古运河，西临京杭运河，成“双河抱湖”之势。小北湖是镶嵌在济宁大地上的一颗明珠，是独成一体的一块金三角。经过十几年的开发治理，集旅游、养殖于一体，已初具规模。小北湖水源充沛、日照充足、碧波荡漾，草丰鱼跃，荷花争艳，风景秀丽，气候宜人。湖内盛产驰名中外的武昌鱼、鲤鱼、鳜鱼、四鼻鲤鱼、甲鱼和虾、毛蟹等。湖区堤岸平直，一般水深1.5—2米，源底平坦，湖中有七个小岛。每年济宁市“荷花会”在此举行，吸引大批中外来客赏荷、垂钓、观光、旅游。</p>
                        <p>相传唐天宝年间，如今太白湖的位置上有一条许家街。那年夏天，诗仙李白夜宿黄梅蔡山江心寺，见蔡山独立江中，一寺高耸入云，不由诗兴大发。令人拿酒来，准备饮酒赋诗。话刚出口，便意识到江心寺乃佛教寺庙，不敢违规。第二天一早，李白使让船主以最快的速度顺风顺水向黄梅县城划去。十几里后，便到了许家街。李白在街东头的一家酒店里，一口气喝了八大碗之后，醉眼朦胧中在酒店的墙壁上写下了著名诗篇《夜宿江心寺》：危楼高百尺，手可摘星辰。不敢高语声，恐惊天上人。从此后“许家街”就被人称做“太白街”了。若干年后，由于地陷，太白街沉下去了，变成了现今风景如画的太白湖。清人石灿《太白湖晚渡》有诗为证：“波光如镜接遥天，极目平湖万顷连。断续渔歌随岸转，清闲鸥鸟聚沙园。淡烟斜抹舒城柳，夕照频催濯港船。更有隔山山色好，几回搔首水云天。”</p>
