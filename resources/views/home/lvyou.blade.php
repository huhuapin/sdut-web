                        <p>孟庙位于山东邹县城南，西与孟府毗邻，是祭祀孟子的庙宇。孟庙始建于北宋（1037年），原址位孟子陵墓旁，宣和三年（1121年）迁建于现址，至明代具现在规模。</p>

                        <p>孟子名轲，儒学正统继承者，有“亚圣”之誉，战国时期著名的思想家、教育家。去世后，葬于邹县郊外，称孟林。县城内又建孟庙、孟府。孟府专供孟子后裔、世袭翰林院五经博士居住。</p>
                        　
                        <p>孟庙呈长方形，前后分五进院落，共有各类殿宇64间，木坊4座，石坊1座。以主体建筑亚圣殿为中心，南北成一中轴线，左右作对称式配列。前部为二十大院，后部分左、中、右三路。亚圣殿重檐九脊，绿琉璃瓦盖顶，朱漆彩绘柱梁。殿内正中供奉孟轲像。楣上有清雍正皇帝御书的“守先待后”横匾。</p>

                        <p>庙内的亚圣殿、寝殿、启圣殿、孟母殿、及御碑亭，各具特色。庙内有碑碣350多块，“孟母断机处”和“孟母三迁”二块，最为引人注目。孟母殿在庙的东北隅，殿前有一石碑，上镌“母教一人”大字。</p>
