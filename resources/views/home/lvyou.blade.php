                        <p>太白楼坐落在济宁市城区古运河北岸，原是唐代贺兰氏经营的酒楼。唐开元二十四年（736年），大诗人李白与夫人许氏及女儿平阳由湖北安陆迁居任城（济宁），“其居在酒楼前”，每天至此饮酒，挥洒文字，写下了许多诗篇。贺兰氏酒楼也因李白经常光顾而名声大振，生意兴隆。自唐咸通二年（861年），吴兴人沈光敬慕李白，登贺兰氏酒楼观光，为该楼篆书“太白酒楼”匾额，并作《李翰林酒楼记》，从此贺兰氏酒楼便改为“太白酒楼”而闻名于世。宋、金、元时期对酒楼均依貌整修。明洪武二十四年（1391年），济宁左卫指挥使狄崇在重建“太白酒楼”时，以“谪仙”的寓意，依原楼的样式，移迁于南城墙上，并将“酒”字去掉，名为“太白楼”，后于明、清、民国间进行了数十次较大的重修。</p>
                        <p>今天的太白楼是1952年人民政府拨专款在旧城墙上重建的。重建后的太白楼连同台基，共占地4000平方米，楼体为两层，重檐歇山式样，砖木结构，二层檐下正中悬扇形“太白楼”楷书阴刻匾额。楼上正厅北壁上方镶有明代诗人所书“诗酒英豪”大字石匾，下嵌着李白、杜甫、贺知章全身阴刻“三公画像石”，楼的游廊和院内有《李白任城厅壁记》和唐代以来文人墨客的赞词、诗赋及乾隆皇帝《登太白楼》等碑碣60余块。有罕见的李白手书“壮观”斗字方碑。1987年，济宁市在太白楼建立李白纪念馆。</p>
