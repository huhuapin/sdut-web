                        <p>梁山因一部古典名著《水浒传》蜚声海内外，梁山好汉的仗义行侠名传近千年，其悠久的历史和深厚的文化积淀，使之成为中华文化园中一支绚丽的奇葩。</p>
                        <p>远古梁山，先民稼穑渔猎，留下了青堌堆和贾堌堆龙山文化遗址。</p>
                        <p>秦汉时期最早设良邑，古名良山，汉初为皇家猎场，《史记·梁孝王世家》载：“（梁王）尝北猎良山”。公元前144年六月，梁王在良山围猎之时，突发病热而卒，后葬于山麓，后易名为梁山。</p>
                        <p>隋唐时期，佛教文化盛极一时，古时有“村庄三十六，寺庙七十二”之说，现存有莲台寺和法兴寺遗址，至今香火不断。</p>
                        <p>唐末宋初因黄河多次泛滥决口，洪水围山成泊，北宋末水域最阔，史称八百里梁山泊，水浒故事就是发生在这个时期，108将据水险山势演绎了一幕幕惊天动地的历史活剧。</p>
                        <p>八百年沧海桑田，后因黄河改道梁山水泊逐渐干枯，但以水浒流传的好汉故事却源远流传，至今景区内水浒遗迹遗址众多，我们梁山旅游区也成为全国唯一一处以农民起义遗址为载体的AAAA级旅游景点。吸引着海内外的游者前来体验水浒文化，领略忠义豪情。</p>
