                        <p>石门山位于曲阜城东北25公里处，西距104国道3公里，原名龙门山，因两山对峙，形如石门，故名。石门山由八个山头组成，突兀的褐色岩石，古建筑的凌空飞檐点缀其中，宛若一幅充满神韵的画卷。 早在唐天宝四年（公元745年），李白和杜甫曾在石门峰台设宴话别；被此山二十四景所吸引的清代著名戏剧家孔尚任，叠石结 ，先后两次在此隐居22年，并酝酿了不朽之作《桃花扇》。现已被定为国家级森林公园。 石门山，有冰雪涧、藤花幄等二十四景，尤以石门月霁著称。山峰峻秀，洞壑深幽，林木葱茏，风光秀丽，被列为国家森林公园。</p>
