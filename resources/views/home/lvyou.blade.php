                        <p>颜庙又称复圣庙，位于山东省曲阜城北门内，陋巷街北首与孔府后花园隔街遥对，是祭祀颜回的祠庙。颜庙始建年代不详。原建于曲阜城东北三公里的五泉庄附近。元初重建，经十年落成。元大德末，旧庙毁。公元1317年(元延祐四年)迁其至陋巷故址。 公元1594年(明万历二十二年)修建，奠定现存规模。颜庙已有680年的历史。2001年被国务院公布为 第五批全国重点文物保护单位，2006年12月被国 家文物局列为世界文化遗产“三孔”的扩展项目，进入《中国世界文化遗产预备名单》。</p>
